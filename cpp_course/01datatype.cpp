#include <iostream>
using namespace std;
int main()
{
    int a;
    cout << "size of int " << sizeof(a) << endl;
    float b;
    cout << "size of float " << sizeof(b) << endl;
    double c;
    cout << "size of double " << sizeof(c) << endl;
    char d;
    cout << "size of char " << sizeof(d) << endl;
    bool e;
    cout << "size of bool " << sizeof(e) << endl;
    short int f;
    cout << "size of short int " << sizeof(f) << endl;
    long g;
    cout << "size of long int " << sizeof(g) << endl;
    return 0;
}